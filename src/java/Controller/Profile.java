    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        ReaderDAO rDAO = new ReaderDAO();
        String username = request.getParameter("username");
        Reader r = rDAO.getPersonByUser(username);
        request.setAttribute("username", username);
        request.setAttribute("r", r);
        request.getRequestDispatcher("profile.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("Name");
        String gender = request.getParameter("Gender");
        String dateOfBirth = request.getParameter("DateOfBirth");
        String address = request.getParameter("Address");
        String email = request.getParameter("Email");
        String phone = request.getParameter("Phone");
        String username = request.getParameter("username");
        int pid = Integer.parseInt(request.getParameter("pid"));
        ReaderDAO rDAO = new ReaderDAO();
        rDAO.updateReader(pid, name, gender, dateOfBirth, null, address, email, phone);
        request.getSession().setAttribute("msg", "Cập nhật thành công!");
        response.sendRedirect("profile?username=" + username);

    }

